def get_unique_subjects_for_weekday(weekday: int) -> list[str]:
    """
    Возвращает список уникальных предметов для данного дня недели,
    сохраняя порядок первого появления.
    """
    subjects = SCHEDULE.get(weekday, [])
    seen = set()
    unique = []
    for s in subjects:
        if s not in seen:
            seen.add(s)
            unique.append(s)
    return unique


